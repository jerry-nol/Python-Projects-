

#parent class
class Animal:
    #behaviors of animals    
    legs = 'I can walk'
    eat = 'I eat food'
    sleep = 'I need to sleep'

#child class
class Dog(Animal):
        #behaviors of a dog
    play = 'I like to play fetch'
    tailWag = 'I wag my tail when I\'m happy'


#child class
class Cat(Animal):
    #behaviors of a cat
    iFit = 'I like to fit in any tight space'
    scratching = 'I need to groom my nails and mark territory'
